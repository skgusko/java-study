package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");

		// \t: tab
		// \r: carriage
		// \n: newline
		// \b: beep
		
		// "hello"
		System.out.println("\"hello\"");
		
		// '
		char c = '\'';
		System.out.println(c);
	}

}
